    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (cbuser.getText().equals("") || cbpass.getText().equals("") || cbnm.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Data harus terisi semua");
        }else{
        try{
            PreparedStatement ps = con.prepareStatement("INSERT INTO petugas VALUES(?,?,?,?,?)");
            ps.setInt(1, 0);
            ps.setString(2, cbuser.getText());
            ps.setString(3, cbpass.getText());
            ps.setString(4, cbnm.getText());
            ps.setString(5, cblevel.getSelectedItem().toString());
            ps.executeUpdate();
            
            
            JOptionPane.showMessageDialog(this,"Data telah tersimpan");
            refreshTable();
        }catch (Exception e) {
            System.out.print("ERROR KUERI KE DATABASE:\n "+e+"\n\n");
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


    private void tblpetugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpetugasMouseClicked
        // TODO add your handling code here:
        int i = tblpetugas.getSelectedRow();
        if (i == -1){
            return;
        }
        
        String user = (String) tm.getValueAt(i, 0);
        iduser=user;
        
        String username = (String) tm.getValueAt(i, 1);
        cbuser.setText(username);
        
        String pass = (String) tm.getValueAt(i, 2);
        cbpass.setText(pass);
        
        String namauser = (String) tm.getValueAt(i, 3);
        cbnm.setText(namauser);
        
        String lvl = (String) tm.getValueAt(i, 4);
        cblevel.setSelectedItem(lvl);
    }//GEN-LAST:event_tblpetugasMouseClicked


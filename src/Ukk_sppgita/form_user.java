    public void refreshTable(){
        tm=new DefaultTableModel(null, new Object[] {"No", "Username", "Password", "Nama User", "Level"});
        tblpetugas.setModel(tm);
        tblpetugas.removeColumn(tblpetugas.getColumnModel().getColumn(0));
        tm.getDataVector().removeAllElements();
        try{
            PreparedStatement s = con.prepareStatement("SELECT * FROM petugas");
            ResultSet r = s.executeQuery();
            while(r.next()){
                Object[] data={
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5)
                };
                tm.addRow(data);
                }
            }catch (Exception e){
                System.out.print("ERROR KUERI KE DATABASE:\n "+e+"\n\n");
        }
    }
    


